const a = 7
let b = 3

b += a  // Mesma coisa que: b = b + a  Adição
console.log(b)

b -= a  // b = b - a  Subtração
console.log(b)

b *= a  // b = b * a  Multiplicação
console.log(b)

b /= a // b = b / a  Divisão
console.log(b)

b %= a // b = b % a  Modulo.  Que é o resto da divisão entre os números.
